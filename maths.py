#MATHS IN PYTHON
# BASIC ARITHmETIC OPERATORS
friends=9
friends+=1 # This is the same as friends = friends + 1
print(f"You have {friends} friends") # Output: 10   

#ADDITION
a = 5
b = 3   
sum = a + b
print(f"The sum of {a} and {b} is: {sum}") # Output: The sum of 5 and 3 is: 8

#SUBTRACTION
difference = a - b  
print(f"The difference between {a} and {b} is: {difference}") # Output: The difference between 5 and 3 is: 2    

#MULTIPLICATION
product = a * b
print(f"The product of {a} and {b} is: {product}") # Output: The product of 5 and 3 is: 15  

#DIVISION
quotient = a / b    
print(f"The quotient of {a} divided by {b} is: {round(quotient, 2)}") # Output: The quotient of 5 divided by 3 is: 1.67

#FLOOR DIVISION
floor_division = a // b
print(f"The floor division of {a} by {b} is: {floor_division}") # Output: The floor division of 5 by 3 is: 1

#MODULUS
remainder = a % b
print(f"The modulus of {a} by {b} is: {remainder}") # Output: The modulus of 5 by 3 is: 2   

#EXPONENTIATION
power = a ** b  
print(f"{a} raised to the power of {b} is: {power}") # Output: 5 raised to the power of 3 is: 125   

