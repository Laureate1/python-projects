#AREA OF A RECTANGLE
#The area of a rectangle is calculated by multiplying the length and width of the rectangle.
#The formula for the area of a rectangle is: 
# Area = Length x Width
#Writing a Python program to calculate the area of a rectangle. 
#Take input from the user for length and width, and then calculate and print the area.
#Take input from the user
length = float(input("Enter the length of the rectangle: "))
width = float(input("Enter the width of the rectangle: "))
#Calculate the area
area = length * width
#Print the area
print(f"The area of the rectangle is: {area}Cm^2")
