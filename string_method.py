# string methods are functions that perform specific operations on strings. They are built-in functions that can be called on string objects to manipulate or analyze the string in various ways. Some common string methods include:
# - upper(): Converts the string to uppercase
# - lower(): Converts the string to lowercase
# - capitalize(): Converts the first character to uppercase
# - title(): Converts the first character of each word to uppercase
# - strip(): Removes leading and trailing whitespace
# - find(): Returns the index of the first occurrence of a substring
# - replace(): Replaces all occurrences of a substring with another substring
# - split(): Splits the string into a list of substrings
# - join(): Joins a list of strings into a single string
