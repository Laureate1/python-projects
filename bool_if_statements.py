# IF STATEMENTS WITH BOOLEAN VALUES
# In Python, you can use boolean values (True and False) in if statements to control the flow of your program. An if statement will execute a block of code only if a certain condition is true. Here are some examples of how to use boolean values in if statements:  
#e.g1
for_sale = True
if for_sale:
    print("This item is for sale.")
else:
    print("This item is Not for sale.")